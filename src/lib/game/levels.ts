// Definicion de los niveles del mini juego "Jafey Sojo y el Estadio Rosabal".
// Cada tile mide 16x16 px. Convencion de caracteres:
//
//   . = aire
//   # = bloque solido (adoquin de calle)
//   = = plataforma (bandera decorativa)
//   B = ladrillo rompible con moneda (escudo CSH)
//   ? = bloque sorpresa (moneda o power-up)
//   E = enemigo caminador ("saprisista")
//   L = enemigo saltarin ("liguista")
//   C = moneda suelta (escudito CSH)
//   I = Imperial (invencibilidad temporal)
//   S = Botines dorados (doble salto)
//   J = Camiseta CSH (escudo 1 golpe)
//   P = spawn del jugador
//   G = meta (puerta del estadio)

